						<h4>Introduction / Level 4</h4>
<p>Learn basic Hatha Yoga and how to utilize props. Find alignment and ease as you explore the introductory poses and basic concepts of yoga.</p>
<h4>LEVEL 2</h4>
<p>Expand your practice by exploring more in depth poses, including supported inversions such as shoulder stand. A variety of props are used to encourage physical alignment and maximize body-mind integration.</p>

<h4>VINYASA YOGA</h4>
<p>A creative sequence of yoga poses that integrated movement and breath into a flowing practice. Physical endurance is required along with knowledge of yoga poses. This is a moderate intensity class.</p>

<h4>ASHTANGA YOGA</h4>
<p>A systematic progression of yoga vinyasa that integrates breathing exercises with energy guides and core stabilizers to expand your practice. This is a moderate to vigorous intensity class.</p>

<h4>SUN SALUTATIONS</h4>
<p>The classic vinyasa practice. Just a 45-minute class—it's the perfect way to wake up and still get to work in time. In the summer, this class is offered on Spring Lake at Lakeside Beach. Suitable for all abilities.</p>

<h4>RESTORATIVE YOGA</h4>
<p>This class focuses on deep relaxation and revitalization of the body, mind and spirit. Utilizing props, poses will be held for longer duration as the body is nourished and supported, allowing for complete relaxation and tension release. Breathing techniques (pranayama) and some guided meditation are included to enhance your personal practice. Aromatherapy and gentle massage will be used to complete the relaxing atmosphere.</p>

<h4>CHAIR YOGA</h4>
<p>Incorporates the use of a chair as an optional aid to provide support. This class does not include floor work. Great for beginners, anyone that has physical limitations or compromised balance, and women in advanced pregnancy.*</p>

<h4>GENTLE YOGA</h4>
<p>A slow progression of relaxation, stretching, and basice poses which enhance the body-mind-spirit interconnection. Perfect for all abilities, also ideal as a prenatal* practice or as a first step in an integrative yoga practice.</p>

